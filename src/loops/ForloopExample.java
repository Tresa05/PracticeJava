package loops;

public class ForloopExample {
int num;


//print the next 10 numbers
void printConsecutive10Numbers() {
	//Syntax of for loop //for (int i=0;i<5;i++)
	//(initialize counter; checkcondition;increment counter
	
	for(int i=0;
			    i<10;
			    i++) {
		
		System.out.println(num++); 
		
		
	}
}
}
